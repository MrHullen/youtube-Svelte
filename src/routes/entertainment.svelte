<h2>PewDiePie</h2>
<h3>109 million subs</h3>
<p>
Felix Arvid Ulf Kjellberg (born 24 October 1989), better known by 
his online username PewDiePie, is a Swedish Gaming/Commentary channel 
on YouTube. His content has changed significantly since he began uploading, 
now focusing on Minecraft gaming videos and established series such as
 'You Laugh You Lose' and 'Last Week I Asked You (LWIAY)' where he reviews
  memes that his fans have posted on Reddit. He has over 100 million subscribers
   as of February 2021. On 15 August 2013, his channel became the most subscribed
    channel on YouTube. He was also the first YouTuber to get 14 million subscribers.
</p>

<h2>SET India</h2>
<h3>96 million subs</h3>
<p>
Sony Entertainment Television (abbr. SET) is an Indian Hindi-language general 
entertainment pay television channel that was launched on November 21, 1995, 
and is owned by Sony Pictures Networks India, a subsidiary of the Japanese Sony.

SET has been one of India's most popular television channels, having produced 
many iconic shows such as CID, Aahat, Indian Idol, Super Dancer, Crime Patrol, 
Beyhadh, Jassi Jaissi Koi Nahin, The Kapil Sharma Show, Yeh Un Dinon Ki Baat Hai, 
Bade Achhe Lagte Hain and Kuch Rang Pyar Ke Aise Bhi. It is also home of Kaun Banega 
Crorepati based on British programme Who Wants to Be a Millionaire? 

SET India's YouTube channel also has over 96 million subscribers, making it the 4th 
most subscribed YouTube channel overall.
</p>

<h2>✿ Kids Diana Show</h2>
<h3>74 million subs</h3>
<p>
Kids Diana Show (stylized as ✿ Kids Diana Show) is a YouTube channel featuring a girl, 
Diana playing with her family and friends. As of January 2021, the channel has around 
73.9 million subscribers and around 50.6 billion views, making it the most-subscribed 
YouTube channel from Ukraine, the 5th largest YouTube channel, the 4th largest YouTube 
channel made for kids and the 2nd largest independent non-corporate YouTube channel. 
It is also currently the 3rd most viewed YouTube channel in the world.

Diana's early videos focus on children's songs, learning English, reviewing toys, and 
vlogging. Many of her videos focus on her in "pretend play," or dressing up as a princess. 
She also has a number of music videos, with songs created just for kids. Her most-watched video,
 "Diana and her Barbie Car - Camping Adventure" has over 1 billion views.  They have
  also collaborated with popular kids channels Like Nastya and Vlad and Niki.
</p>

